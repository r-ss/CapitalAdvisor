        let percent:Double = 0.10
        
        let totalIncome = stocks.totalIncomesValueInCurrency(.RUB)
        
        var monthly:Double = 10000
        if totalIncome > 1000 {
            monthly = totalIncome * 0.15
        }
        
        for _ in 0...9 {
            total += monthly * 12
            let devidends = total * percent
            totalDevidents += devidends
