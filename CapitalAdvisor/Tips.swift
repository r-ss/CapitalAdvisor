            let long = ""
            let tip = Tip(intro: "–°—É–º–º–∞ –≤–∞—à–∏—Ö –Ω–∞–∫–æ–ø–ª–µ–Ω–∏–π –º–µ–Ω—å—à–µ —Ç—Ä–µ—Ö–∫—Ä–∞—Ç–Ω–æ–π —Å—É–º–º—ã –≤–∞—à–µ–≥–æ –µ–∂–µ–º–µ—Å—è–∂–Ω–æ–≥–æ –¥–æ—Ö–æ–¥–∞. –†–µ–∫–æ–º–µ–Ω–¥—É–µ—Ç—Å—è –∏–º–µ—Ç—å –Ω–∞–∫–æ–ø–ª–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã—Ö –±—É–¥–µ—Ç –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –∫–∞–∫ –º–∏–Ω–∏–º—É–º –Ω–∞ —Ç—Ä–∏ –º–µ—Å—è—Ü–∞ –∂–∏–∑–Ω–∏ –ø—Ä–∏ –≤–∞—à–µ–º –æ–±—ã—á–Ω–æ–º —É—Ä–æ–≤–Ω–µ —Ä–∞—Å—Ö–æ–¥–æ–≤.", teaser: "–ü–æ–¥—Ä–æ–±–Ω–µ–µ", text: long)
