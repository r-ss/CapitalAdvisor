        let currentTotal = stocks.totalStocksValueInCurrency(.RUB)
        
        var total = currentTotal
        var totalDevidents:Double = 0
        
        let percent:Double = 0.10
        
        let totalIncome = stocks.totalIncomesValueInCurrency(.RUB)
        
        var monthly:Double = 10000
        if totalIncome > 1000 {
            monthly = totalIncome * 0.15
        }
        
        for _ in 0...9 {
            total += monthly * 12
            let devidends = total * percent
            totalDevidents += devidends
            total += devidends
            //print("index: \(index), devidends: \(devidends) total:\(total)")
        }
        
        let tip1 = Tip(intro: "–ï—Å–ª–∏ –≤—ã –Ω–∞—á–Ω–µ—Ç–µ –∏–Ω–≤–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å —Å–≤–æ–π –∫–∞–ø–∏—Ç–∞–ª <em>_\(valueFormat.format(currentTotal, currency: .RUB))_</em> –ø–æ–¥ 10% –≥–æ–¥–æ–≤—ã—Ö –∏ –µ–∂–µ–º–µ—Å—è—á–Ω–æ –±—É–¥–µ—Ç–µ –¥–æ–±–∞–≤–ª—è—Ç—å –∫ —Å—É–º–º–µ 15% –æ—Ç –≤–∞—à–∏—Ö –¥–æ—Ö–æ–¥–æ–≤ <em>_(\(valueFormat.format(monthly, currency: .RUB)))_</em>, —Ç–æ —á–µ—Ä–µ–∑ –¥–µ—Å—è—Ç—å –ª–µ—Ç –≤–∞—à –∫–∞–ø–∏—Ç–∞–ª —Å–æ—Å—Ç–∞–≤–∏—Ç <em>_\(valueFormat.format(total, currency: .RUB))_</em> –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö <em>_\(valueFormat.format(stocks.totalStocksValueInCurrency(.RUB) + (monthly * 12 * 10), currency: .RUB))_</em> ‚Äî –≤–ª–æ–∂–µ–Ω–Ω—ã–µ –≤–∞–º–∏ –¥–µ–Ω—å–≥–∏ –∏ <em>_\(valueFormat.format(totalDevidents, currency: .RUB))_</em> ‚Äî –¥–µ–≤–∏–¥–µ–Ω–¥—ã", teaser:"–£–∑–Ω–∞—Ç—å –±–æ–ª—å—à–µ", text: long)
        
        let tip2 = Tip(intro: "–ï—Å–ª–∏ –≤—ã –Ω–∞—á–Ω–µ—Ç–µ –∏–Ω–≤–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å —Å–≤–æ–π –∫–∞–ø–∏—Ç–∞–ª <em>_\(valueFormat.format(currentTotal, currency: .RUB))_</em> –ø–æ–¥ 10% –≥–æ–¥–æ–≤—ã—Ö –∏ –µ–∂–µ–º–µ—Å—è—á–Ω–æ –±—É–¥–µ—Ç–µ –¥–æ–±–∞–≤–ª—è—Ç—å –∫ —Å—É–º–º–µ <em>_\(valueFormat.format(monthly, currency: .RUB))_</em>, —Ç–æ —á–µ—Ä–µ–∑ –¥–µ—Å—è—Ç—å –ª–µ—Ç –≤–∞—à –∫–∞–ø–∏—Ç–∞–ª —Å–æ—Å—Ç–∞–≤–∏—Ç <em>_\(valueFormat.format(total, currency: .RUB))_</em> –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö <em>_\(valueFormat.format(stocks.totalStocksValueInCurrency(.RUB) + (monthly * 12 * 10), currency: .RUB))_</em> ‚Äî –≤–ª–æ–∂–µ–Ω–Ω—ã–µ –≤–∞–º–∏ –¥–µ–Ω—å–≥–∏ –∏ <em>_\(valueFormat.format(totalDevidents, currency: .RUB))_</em> ‚Äî –¥–µ–≤–∏–¥–µ–Ω–¥—ã", teaser:"–£–∑–Ω–∞—Ç—å –±–æ–ª—å—à–µ", text: long)
        
